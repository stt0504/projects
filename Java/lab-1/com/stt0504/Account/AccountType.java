/**
 * The package Entities.Account contains classes and enumerations related to user accounts.
 */
package com.stt0504.Account;

/**
 * The AccountType enumeration defines types of user accounts: credit, debit and deposit.
 */
public enum AccountType {
    /** Represents a credit account. */
    CREDIT,

    /** Represents a debit account. */
    DEBIT,

    /** Represents a deposit account. */
    DEPOSIT
}
